package com.RestTemplate.RTemplateDemo;

public class User3 {

    private Country  country ;
     private String name;

    public User3() {
    }

    public User3(Country country, String name) {
        this.country = country;
        this.name = name;
    }

    public Country getCountry() {
        return country;
    }

    public String getName() {
        return name;
    }
}






